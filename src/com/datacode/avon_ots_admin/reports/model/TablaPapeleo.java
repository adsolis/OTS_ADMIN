	public void setPanoram(int panoram) {
		this.panoram = panoram;
	}
	/**
	 * @return the portaf
	 */
	public int getPortaf() {
		return portaf;
	}
	/**
	 * @param portaf the portaf to set
	 */
	public void setPortaf(int portaf) {
		this.portaf = portaf;
	}
	/**
	 * @return the papGerenZonal
	 */
